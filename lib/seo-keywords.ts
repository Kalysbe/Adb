// Функция для получения ключевых слов по типу страницы
export function getKeywordsByType(
  type: "general" | "audit" | "accounting" | "tax" | "business" | "contacts" | "about",
): string[] {
  switch (type) {
    case "general":
      return generalKeywords
    case "audit":
      return auditKeywords
    case "accounting":
      return accountingKeywords
    case "tax":
      return taxConsultingKeywords
    case "business":
      return businessConsultingKeywords
    case "contacts":
      return contactsKeywords
    case "about":
      return aboutKeywords
    default:
      return generalKeywords
  }
}

// Функция для получения случайных ключевых слов из списка
export function getRandomKeywords(
  type: "general" | "audit" | "accounting" | "tax" | "business" | "contacts" | "about",
  count = 5,
): string[] {
  const keywords = getKeywordsByType(type)
  const shuffled = [...keywords].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, count)
}
